    private void cargarTabla() {

        modelo = new DefaultTableModel();
        modelo.addColumn("Clientes");

        // SI NO HAY CLIENTES
        if (AppPracticasSwing.clientes.isEmpty()) {
            modelo.addRow(new Object[]{"--- No existen clientes registrados ---"});
            jtListadoCliente.setEnabled(false); // bloquear selecci√≥n
        } else {

            for (String c : AppPracticasSwing.clientes) {
                modelo.addRow(new Object[]{c});
            }

            jtListadoCliente.setEnabled(true);
        }

        jtListadoCliente.setModel(modelo);

        sorter = new TableRowSorter<>(modelo);
        jtListadoCliente.setRowSorter(sorter);
    }

